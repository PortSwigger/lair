package lair.models;

import java.util.Objects;

/**
 *
 * @author dkottmann
 */
public class IssueReference {
    private String link;
    private String name;
    
    public IssueReference() {
        this.link = "";
        this.name = "";
    }

    /**
     * @return the link
     */
    public String getLink() {
        return link;
    }

    /**
     * @param link the link to set
     */
    public void setLink(String link) {
        this.link = link;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 97 * hash + Objects.hashCode(this.link);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final IssueReference other = (IssueReference) obj;
        if (!Objects.equals(this.link, other.link)) {
            return false;
        }
        return true;
    }
}
